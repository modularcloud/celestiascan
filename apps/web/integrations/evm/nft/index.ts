export async function NFTExtract(_q: unknown, metadata: EngineConfigMetadata) {
  const query = z.string().parse(_q);
  const [contractAddress, tokenId, balance] = query.split(":");
  const mc = createModularCloud(env.EVM_CHAIN_DATA_SERVICE);
  const web3 = new Web3(metadata.endpoint);
  const tokenType = await mc.evm
    .describeContract(metadata.network.id, contractAddress)
    .then((res) => res.type);
  const [tokenUri, name, symbol] = (
    tokenType === "erc721"
      ? await Promise.all([
          new web3.eth.Contract(Erc721ABI, contractAddress).methods
            .tokenURI(tokenId)
            .call(),
          new web3.eth.Contract(Erc721ABI, contractAddress).methods
            .name()
            .call(),
          new web3.eth.Contract(Erc721ABI, contractAddress).methods
            .symbol()
            .call(),
        ])
      : tokenType === "erc1155"
      ? await Promise.all([
          new web3.eth.Contract(Erc1155ABI, contractAddress).methods
            .uri(tokenId)
            .call(),
          Promise.resolve(undefined),
          Promise.resolve(undefined),
        ])
      : await Promise.reject(new Error("Invalid token type"))
  ) as [string, string | undefined, string | undefined];

  const uri =
    tokenType === "erc1155"
      ? tokenUri.replace("{id}", Web3.utils.padLeft(tokenId, 64))
      : tokenUri;
  const md = await Promise.any([
    fetch(convertToHttpIfIpfs(uri))
      .then((res) => res.json())
      .then((res) => {
        return MetadataSchema.parse(res);
      }),
    fetch(convertToPublicHttpIfIpfs(uri))
      .then((res) => res.json())
      .then((res) => {
        return MetadataSchema.parse(res);
      }),
  ]).then(async (res) => {
    if (res) {
      const fimg = await Promise.any([
        fetch(convertToHttpIfIpfs(res.image)),
        fetch(convertToPublicHttpIfIpfs(res.image)),
      ]);
      const fimgb = Buffer.from(await fimg.arrayBuffer());
      const result = await uploadFile(fimgb, {
        publicKey: env.UPLOADCARE_API_KEY as string,
        store: "auto",
        metadata: {
          original: res.image,
        },
      });
      return {
        ...res,
        image: result.cdnUrl,
        raw: JSON.stringify(res, null, 2),
      };
    }
  });

  return {
    address: contractAddress,
    id: tokenId,
    metadata: md,
    uri,
    tokenType,
    name,
    symbol,
    balance,
  };
}

export const NFTLoader = createLoader()
  .addExtract(NFTExtract)
  .addTransform(CardTransform)
  .addTransform(RowTransform)
  .addTransform(AssociatedTransform)
  .addTransform(PageTransform)
  .addTransform(RawTransform)
  .addTransform(SidebarTransform)
  .addTransform(TopbarTransform)
  .finish();
